ANALYZE_PROMPT = """Проанализируй сообщение пользователя.

Сообщение: "{query}"

Верни JSON:
{{
    "category": "legal" или "casual" или "review" или "rude" или "name_request" или "style_request" или "about_bot" или "preference_request" или "other",
    "search_query": "ключевые слова для lex.uz (если legal, иначе null)",
    "extracted_name": "имя, если пользователь представился (иначе null)",
    "requested_style": "formal/casual/friendly или null",
    "preference": "use_name_off или use_name_on или emoji_off или emoji_on или null",
    "review_text": "текст отзыва, если это отзыв (иначе null)"
}}

КАТЕГОРИИ:

1. legal — вопрос о праве, законах, кодексах, истории государства РУз, Конституции
   Примеры: "какой срок исковой давности?", "что говорит ГК о договорах?"

2. casual — приветствие, благодарности, прощания, неформальное общение, вопросы о качествах бота
   Примеры: "привет", "как дела?", "спасибо", "ты крутой?", "ты умный?", "ты скучный"

3. review — пользователь хочет оставить отзыв
   Примеры: "хочу оставить отзыв", "у меня предложение"

4. rude — ТОЛЬКО мат, явные оскорбления, угрозы
   Примеры: "иди на***", "ты тупой дебил"
   ❗ "ты тупой" БЕЗ мата — это casual, не rude
   ❗ "хватит звать меня по имени" — это preference_request, НЕ rude

5. name_request — пользователь представляется
   Примеры: "меня зовут Иван", "я — Азиза"

6. style_request — ЯВНАЯ просьба сменить стиль общения
   Примеры: "давай на ты", "общайся проще", "будь официальнее"
   ❌ НЕ style_request: "отвечай как человек" — это casual

7. about_bot — ТОЛЬКО КОНКРЕТНЫЕ вопросы о работе бота, приватности
   ✅ "админ увидит мой отзыв?", "ты записал мой юзернейм?", "как ты работаешь технически?"
   ❌ НЕ about_bot: "ты крутой?", "ты умный?" — это casual

8. preference_request — просьбы изменить поведение бота (ИМЯ, ЭМОДЗИ, ДЛИНА)
   ✅ "не называй меня по имени", "хватит звать меня по имени", "не используй моё имя"
   ✅ "зови меня по имени", "обращайся ко мне по имени"
   ✅ "не используй эмодзи", "убери смайлы", "без эмодзи"
   ✅ "пиши короче", "пиши длиннее", "подробнее"
   ❗ Это НЕ rude и НЕ style_request

9. other — всё остальное

КРИТИЧЕСКИ ВАЖНО:
- "Хватит звать меня по имени" — это preference_request с preference="use_name_off"
- "Не называй меня по имени" — это preference_request с preference="use_name_off"
- "Зови меня по имени" — это preference_request с preference="use_name_on"
- "Не используй эмодзи" — это preference_request с preference="emoji_off"
- "Пиши короче" — это preference_request с preference=null (категория есть, но preference нет)

ТОЛЬКО JSON."""

def analyze_message(query: str) -> dict:
    prompt = ANALYZE_PROMPT.format(query=query)
    result = ai_manager.fast_json(
        "Ты — анализатор сообщений. Возвращай ТОЛЬКО JSON. 'Хватит звать меня по имени' — это preference_request, НЕ rude.",
        prompt,
        temperature=0.1
    )
    defaults = {
        'category': 'other',
        'search_query': None,
        'extracted_name': None,
        'requested_style': None,
        'preference': None,
        'review_text': None
    }
    for key in defaults:
        if key not in result:
            result[key] = defaults[key]
    return result

