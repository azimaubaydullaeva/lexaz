def generate_preference_answer(preference: str, user_data: dict = None) -> str:
    """Генерирует ответ на просьбу изменить поведение"""
    
    responses = {
        'use_name_off': [
            "Хорошо, не буду называть вас по имени.",
            "Понял. Обращаюсь без имени.",
            "Принято. Без имени — так без имени.",
            "Хорошо, учту.",
        ],
        'use_name_on': [
            "Хорошо, буду обращаться по имени.",
            "Понял, учту.",
            "Хорошо.",
        ],
        'emoji_off': [
            "Хорошо, без эмодзи.",
            "Понял. Убираю смайлы.",
            "Принято.",
        ],
        'emoji_on': [
            "Хорошо, буду иногда использовать.",
            "Понял.",
        ],
    }
    
    import random
    options = responses.get(preference, ["Понял, учту."])
    return random.choice(options)