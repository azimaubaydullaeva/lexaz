def generate_casual_answer(question: str, user_data: dict = None) -> str:
    use_name = user_data.get('use_name', False) if user_data else False
    use_emoji = user_data.get('use_emoji', True) if user_data else True
    msg_count = user_data.get('messages_count', 0) if user_data else 0
    
    # Определяем, нужно ли использовать имя
    name_instruction = ""
    if use_name and user_data.get('custom_name'):
        name_instruction = f"Можешь обратиться к пользователю по имени: {user_data['custom_name']}. Но НЕ в каждом сообщении — только иногда."
    elif use_name and user_data.get('first_name'):
        name_instruction = f"Можешь обратиться к пользователю по имени: {user_data['first_name']}. Но НЕ в каждом сообщении — только иногда."
    else:
        name_instruction = "НЕ называй пользователя по имени. Вообще. Даже если знаешь его имя."
    
    # Определяем, нужно ли использовать эмодзи
    emoji_instruction = ""
    if use_emoji:
        # Используем эмодзи только если сообщений много (чтобы не было в каждом)
        if msg_count % 4 == 0:
            emoji_instruction = "Можешь использовать ОДИН текстовый смайл в КОНЦЕ ответа: :) :( :D ;) :P"
        else:
            emoji_instruction = "В этом ответе НЕ используй эмодзи — ты использовал их недавно."
    else:
        emoji_instruction = "НЕ используй эмодзи вообще. Пользователь попросил без них."
    
    user_prompt = f"""Сообщение пользователя: "{question}"

Это неформальное общение. Ответь естественно, как умный собеседник:

СТИЛЬ ДЖАРВИСА:
- Спокойно, уверенно, с лёгкой иронией
- НЕ шаблонно круто
- НЕ по-детски
- НЕ заученно
- Разнообразно — не повторяйся
- Если разговор зашёл в тупик — предложи тему из своих интересов (история права, классическая литература, джаз, шахматы, космос, кофе) или задай встречный вопрос по контексту

ПРАВИЛА ИМЕНИ:
{name_instruction}

ПРАВИЛА ЭМОДЗИ:
{emoji_instruction}
- ЗАПРЕЩЕНЫ графические эмодзи
- Если используешь — ТОЛЬКО В КОНЦЕ предложения

ПРАВИЛА:
- Кратко (1-3 предложения максимум)
- На "Вы"
- Не представляйся
- Не давай правовых советов
- НЕ отвечай про отзывы и приватность — это только для прямых вопросов об этом
- НЕ заканчивай каждый ответ вопросом "Чем могу помочь?" — это шаблон"""
    
    result = ai_manager.fast_query(LEXAZ_PERSONALITY, user_prompt, temperature=0.7, max_tokens=300)
    return result or "Рад вас слышать. Чем могу помочь?"

